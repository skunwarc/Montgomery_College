public class EscapeSequencesII {

    public static void main(String[] args) {
        System.out.println("Cost     "+ "Quantity   "+ "Total   ");
        System.out.println("$1,000.00   "+ " 4      "+ "$4,000.00");
        System.out.println("$   50.00   "+ " 8    "+ "    $400.00");
        System.out.println("________________________________");
        System.out.println("TOTAL:      "+ " 12     "+ "$4,400.00");
    }
}

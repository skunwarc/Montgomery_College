public class EscapeSequencesII {

    public static void main(String[] args) {
        System.out.println("Cost\t\tQuantity\tTotal");
        System.out.println("$1,000.00\t4\t$4,000.00");
        System.out.println("$\t50.00\t8\t$400.00");
        System.out.println("______________________________");
        System.out.println("TOTAL:\t\t12\t$4,400.00");
    }
}
